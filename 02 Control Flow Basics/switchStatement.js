//switch statement

let grade = 'A';

switch(grade){

    case 'A':
        console.log('You Got A');
        break;
    case 'B':
        console.log('You Got B');
        break;
    case 'C':
        console.log('You Got C');
        break;
    case 'D':
        console.log('You Got D');
        break;
    case 'E':
        console.log('You Got E');
        break;
    default:
        console.log('You Got Big F . Which Means You Looser');

}